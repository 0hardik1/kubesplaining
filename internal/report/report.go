// bezier returns an SVG cubic-bezier path from (x1,y1) to (x2,y2) with horizontal tangents.
func bezier(x1, y1, x2, y2 int) string {
	dx := (x2 - x1) / 2
	if dx < 20 {
		dx = 20
	}
	return fmt.Sprintf("M %d %d C %d %d, %d %d, %d %d", x1, y1, x1+dx, y1, x2-dx, y2, x2, y2)
}

// textCluster is one logical text-element (title, subtitle, etc.) inside a graph node.
// composeLines stacks clusters vertically using each cluster's leadIn (gap before its
// first baseline) and lineHeight (gap between baselines within the cluster).
type textCluster struct {
	class      string
	lineHeight int
	leadIn     int
	lines      []string
}

// composeLines flattens a stack of textClusters into rendered GraphTextLines and reports
// the total node height needed to fit them all (top edge to bottom padding).
// offsetX is the left text padding inside the node; bottomPad is the gap below the last baseline.
func composeLines(offsetX int, clusters []textCluster, bottomPad int) ([]GraphTextLine, int) {
	var lines []GraphTextLine
	y := 0
	for _, c := range clusters {
		if len(c.lines) == 0 {
			continue
		}
		y += c.leadIn
		for i, txt := range c.lines {
			if i > 0 {
				y += c.lineHeight
			}
			lines = append(lines, GraphTextLine{
				Class:   c.class,
				Text:    txt,
				OffsetX: offsetX,
				OffsetY: y,
			})
		}
	}
	return lines, y + bottomPad
}

// wrapForWidth splits s into one or more lines that fit within maxPx, using
// avgCharPx as a font-class-specific average glyph width. Lines break preferentially
// after spaces and identifier separators ('/', ':', '-', '.', ',') so wrapped
// resource paths stay readable. Long unbreakable runs are hard-cut at the column
// limit. SVG <text> has no auto-wrap, so this is what keeps long titles inside
// their box without losing characters (this tool is educational — full text matters).
func wrapForWidth(s string, maxPx int, avgCharPx float64) []string {
	if s == "" {
		return []string{""}
	}
	if avgCharPx <= 0 || maxPx <= 0 {
		return []string{s}
	}
	maxChars := int(float64(maxPx) / avgCharPx)
	if maxChars < 1 {
		maxChars = 1
	}
	runes := []rune(s)
	if len(runes) <= maxChars {
		return []string{s}
	}
	isBreak := func(r rune) bool {
		switch r {
		case ' ', '/', ':', '-', '.', ',':
			return true
		}
		return false
	}
	var lines []string
	start := 0
	for start < len(runes) {
		if len(runes)-start <= maxChars {
			lines = append(lines, strings.TrimLeft(string(runes[start:]), " "))
			break
		}
		end := start + maxChars
		breakAt := -1
		for i := end; i > start; i-- {
			if isBreak(runes[i-1]) {
				breakAt = i
				break
			}
		}
		if breakAt <= start {
			breakAt = end
		}
		line := strings.TrimRight(string(runes[start:breakAt]), " ")
		lines = append(lines, strings.TrimLeft(line, " "))
		// Skip leading spaces on next line; other separators stay attached to whichever side they belong on.
		for breakAt < len(runes) && runes[breakAt] == ' ' {
			breakAt++
		}
		start = breakAt
	}
	if len(lines) == 0 {
		lines = []string{s}
	}
	return lines
}

// presentRules returns the subset of wanted rule IDs that actually have findings in index, preserving wanted order.
func presentRules(index map[string][]models.Finding, wanted []string) []string {
	out := make([]string, 0, len(wanted))
	for _, r := range wanted {
		if len(index[r]) > 0 {
			out = append(out, r)
		}
	}
	return out
}

// contains reports whether slice s contains value v.
func contains(s []string, v string) bool {
	for _, x := range s {
		if x == v {
			return true
		}
	}
	return false
}

// sortedKeys returns the sorted keys of a string-keyed set.
func sortedKeys(m map[string]bool) []string {
	out := make([]string, 0, len(m))
	for k := range m {
		out = append(out, k)
	}
	sort.Strings(out)
	return out
}

// subjectListStep renders a narrative step that injects a list of subjects/resources.
// For 1–2 items it stays inline as natural prose (proseFmt is a Sprintf template with one %s).
// For 3+ items it switches to a lead-in sentence (listLead) plus a bulleted sublist, capped
// to keep the card compact even with thousands of subjects in a real cluster.
func subjectListStep(listLead, proseFmt string, items []string) NarrativeStep {
	const cap = 5
	if len(items) <= 2 {
		var inline string
		switch len(items) {
		case 0:
			inline = ""
		case 1:
			inline = items[0]
		case 2:
			inline = items[0] + " or " + items[1]
		}
		return NarrativeStep{Text: fmt.Sprintf(proseFmt, inline)}
	}
	if len(items) <= cap {
		return NarrativeStep{Text: listLead, Items: items}
	}
	out := append([]string{}, items[:cap]...)
	out = append(out, fmt.Sprintf("…and %d more", len(items)-cap))
	return NarrativeStep{Text: listLead, Items: out}
}

// pluralizeSimple picks the right word form by count; used when a template func can't be used.
func pluralizeSimple(n int, singular, plural string) string {
	if n == 1 {
		return singular
	}
	return plural
}

// maxInt returns the larger of a and b.
func maxInt(a, b int) int {
	if a > b {
		return a
	}
	return b
}

