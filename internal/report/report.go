// buildHeatmap computes a Resource × Category matrix for the top resources, showing how each
// high-blast-radius object concentrates findings across attack categories.
func buildHeatmap(findings []models.Finding, resourceMap map[string][]models.Finding) []HeatmapRow {
	keys := heatmapCategories()
	type entry struct {
		label    string
		findings []models.Finding
	}
	entries := make([]entry, 0, len(resourceMap))
	for label, fs := range resourceMap {
		if label == "" || label == "-" {
			continue
		}
		entries = append(entries, entry{label: label, findings: fs})
	}
	sort.Slice(entries, func(i, j int) bool {
		a := BuildSummary(entries[i].findings)
		b := BuildSummary(entries[j].findings)
		return compareSummaries(a, b, entries[i].label, entries[j].label)
	})
	if len(entries) > 10 {
		entries = entries[:10]
	}

	rows := make([]HeatmapRow, 0, len(entries))
	for _, e := range entries {
		row := HeatmapRow{Label: e.label, Total: len(e.findings)}
		counts := map[string]int{}
		var top models.Severity = models.SeverityInfo
		for _, f := range e.findings {
			counts[categoryCSSKey(f.Category)]++
			if f.Severity.Rank() > top.Rank() {
				top = f.Severity
			}
		}
		for _, k := range keys {
			n := counts[k.Key]
			row.Cells = append(row.Cells, HeatmapCell{
				Count:    n,
				CatClass: k.Key,
				Level:    heatLevel(n),
				Resource: e.label,
			})
		}
		switch top {
		case models.SeverityCritical:
			row.SevClass, row.SevLabel = "crit", "CRIT"
		case models.SeverityHigh:
			row.SevClass, row.SevLabel = "high", "HIGH"
		case models.SeverityMedium:
			row.SevClass, row.SevLabel = "med", "MED"
		default:
			row.SevClass, row.SevLabel = "low", "LOW"
		}
		rows = append(rows, row)
	}
	return rows
}

// heatLevel bins a cell count into a 0..5 intensity level for CSS-driven heat coloring.
func heatLevel(n int) int {
	switch {
	case n <= 0:
		return 0
	case n == 1:
		return 1
	case n == 2:
		return 2
	case n == 3:
		return 3
	case n == 4:
		return 4
	default:
		return 5
	}
}

// buildNarratives auto-detects well-known attack chains from finding rule_ids and returns plain-English
// walkthroughs to help first-time readers see how individual findings compose into exploit paths.
func buildNarratives(findings []models.Finding) []NarrativeCard {
	narratives := make([]NarrativeCard, 0, 4)

	index := map[string][]models.Finding{}
	for _, f := range findings {
		index[f.RuleID] = append(index[f.RuleID], f)
	}

	// Chain 1: Node root / container escape.
	nodeEscapeRules := []string{"KUBE-ESCAPE-001", "KUBE-ESCAPE-006", "KUBE-ESCAPE-004", "KUBE-ESCAPE-005"}
	nodeEscapeHits := map[string]bool{}
	nodeEscapeResources := map[string]bool{}
	for _, rule := range nodeEscapeRules {
		for _, f := range index[rule] {
			nodeEscapeHits[rule] = true
			if f.Resource != nil {
				nodeEscapeResources[f.Resource.Key()] = true
			}
		}
	}
	if len(nodeEscapeHits) > 0 {
		resList := sortedKeys(nodeEscapeResources)
		sev := "HIGH"
		if nodeEscapeHits["KUBE-ESCAPE-001"] || nodeEscapeHits["KUBE-ESCAPE-006"] {
			sev = "CRITICAL"
		}
		steps := []NarrativeStep{
			subjectListStep("An attacker gains code execution in a workload co-scheduled with, or targeting, one of:",
				"An attacker gains code execution in a workload co-scheduled with, or targeting, %s.", resList),
			{Text: "The workload is configured to trust the host in one or more ways — privileged mode grants all capabilities; a hostPath of / mounts the node's root filesystem; hostPID/hostIPC share the host's process and IPC namespaces."},
			{Text: "Any one of these alone is enough for a straightforward container escape: write into the host filesystem, exec through /proc/1, or interact with the kubelet's unix socket."},
			{Text: "From there, the attacker reads projected tokens for every other pod on the node and pivots into the cluster with those identities."},
		}
		narratives = append(narratives, NarrativeCard{
			Title:    "Privileged workload → node root",
			Severity: sev,
			Steps:    steps,
			RuleIDs:  presentRules(index, nodeEscapeRules),
		})
	}

	// Chain 2: Cluster-admin impersonation via over-permissioned SAs.
	impersonationRules := []string{"KUBE-PRIVESC-001", "KUBE-PRIVESC-005"}
	subjects := map[string]map[string]bool{}
	for _, rule := range impersonationRules {
		for _, f := range index[rule] {
			if f.Subject == nil {
				continue
			}
			key := f.Subject.Key()
			if subjects[key] == nil {
				subjects[key] = map[string]bool{}
			}
			subjects[key][rule] = true
		}
	}
	var dualSubjects []string
	for sub, hits := range subjects {
		if len(hits) == len(impersonationRules) {
			dualSubjects = append(dualSubjects, sub)
		}
	}
	sort.Strings(dualSubjects)
	if len(dualSubjects) > 0 {
		steps := []NarrativeStep{
			subjectListStep("The attacker lands on a workload that mounts one of the following service accounts (or phishes a kubeconfig bound to one):",
				"The attacker lands on a workload that mounts %s, or phishes a kubeconfig bound to it.", dualSubjects),
			{Text: "That identity holds cluster-wide get/list on secrets — including service-account tokens in every namespace. The attacker lists kube-system secrets and reads tokens belonging to powerful controllers."},
			{Text: "Even without the token read, the same identity can create pods cluster-wide. The attacker schedules a pod that mounts the target service account, execs in, and acts as it."},
			{Text: "Either path converges on a cluster-admin-equivalent identity; all policies, secrets, and workloads are now under attacker control."},
		}
		narratives = append(narratives, NarrativeCard{
			Title:    "Token theft → cluster-admin impersonation",
			Severity: "CRITICAL",
			Steps:    steps,
			RuleIDs:  presentRules(index, impersonationRules),
		})
	}

	// Chain 3: Admission-gate bypass — enforcement silently missing.
	admissionRules := []string{"KUBE-ADMISSION-001", "KUBE-ADMISSION-002", "KUBE-ADMISSION-003"}
	presentAdmission := presentRules(index, admissionRules)
	if len(presentAdmission) > 0 {
		steps := []NarrativeStep{}
		if contains(presentAdmission, "KUBE-ADMISSION-001") {
			steps = append(steps, NarrativeStep{Text: "The webhook that should block dangerous pods fails open: failurePolicy: Ignore means any backend outage (or a targeted denial-of-service) silently disables enforcement for the window the attacker needs."})
		}
		if contains(presentAdmission, "KUBE-ADMISSION-003") {
			steps = append(steps, NarrativeStep{Text: "Its namespace selector excludes at least one sensitive namespace — workloads placed there skip admission entirely."})
		}
		if contains(presentAdmission, "KUBE-ADMISSION-002") {
			steps = append(steps, NarrativeStep{Text: "The webhook keys off a workload-controlled label. Omit the label and admission doesn't apply."})
		}
		steps = append(steps, NarrativeStep{Text: "Any one of the above is a full bypass of the admission gate you thought was catching misconfigurations — every other chain in this report becomes easier to execute."})
		narratives = append(narratives, NarrativeCard{
			Title:    "Admission gap → silent enforcement bypass",
			Severity: "HIGH",
			Steps:    steps,
			RuleIDs:  presentAdmission,
		})
	}

	// Chain 4: Flat network → unrestricted lateral reach.
	networkRules := []string{"KUBE-NETPOL-COVERAGE-001", "KUBE-NETPOL-WEAKNESS-001", "KUBE-NETPOL-WEAKNESS-002"}
	presentNetwork := presentRules(index, networkRules)
	if len(presentNetwork) > 0 {
		steps := []NarrativeStep{}
		if contains(presentNetwork, "KUBE-NETPOL-COVERAGE-001") {
			steps = append(steps, NarrativeStep{Text: "Namespaces with no NetworkPolicies treat every pod as reachable from every other pod — there is no default-deny, so a compromised workload can reach every service on every pod."})
		}
		if contains(presentNetwork, "KUBE-NETPOL-WEAKNESS-001") {
			steps = append(steps, NarrativeStep{Text: "An allow-from-all-namespaces policy is effectively no policy: traffic from any namespace matches, including attacker-controlled namespaces."})
		}
		if contains(presentNetwork, "KUBE-NETPOL-WEAKNESS-002") {
			steps = append(steps, NarrativeStep{Text: "Egress 0.0.0.0/0 gives the attacker free outbound reach — stolen tokens, secrets, and staging data leave the cluster with nothing in the way."})
		}
		steps = append(steps, NarrativeStep{Text: "Combined, the attacker sweeps every pod in the cluster for vulnerable services and exfiltrates data without tripping a segmentation boundary."})
		narratives = append(narratives, NarrativeCard{
			Title:    "Flat network → unrestricted lateral reach",
			Severity: "HIGH",
			Steps:    steps,
			RuleIDs:  presentNetwork,
		})
	}

	return narratives
}

// buildHeadline returns a data-driven h1 string and the short prose that sits under it.
// The prose is returned as template.HTML because it contains safe, pre-escaped <strong> and <code> markup.
func buildHeadline(s Summary, narratives []NarrativeCard, ns []Hotspot) (string, template.HTML) {
	criticalChains := 0
	for _, n := range narratives {
		if n.Severity == "CRITICAL" {
			criticalChains++
		}
	}

	var headline string
	switch {
	case criticalChains >= 2:
		headline = fmt.Sprintf("%d independent paths to full cluster takeover", criticalChains)
	case criticalChains == 1:
		headline = "1 path to full cluster takeover detected"
	case s.Critical > 0:
		headline = fmt.Sprintf("%d critical findings require immediate attention", s.Critical)
	case s.High > 0:
		headline = fmt.Sprintf("%d high-severity findings across the cluster", s.High)
	case s.Total > 0:
		headline = fmt.Sprintf("%d findings across the cluster", s.Total)
	default:
		headline = "No findings in this scan"
	}

	var b strings.Builder
	fmt.Fprintf(&b, "%d findings across %d %s.", s.Total, len(narratives), pluralizeSimple(len(narratives), "chain", "chains"))
	if len(ns) > 0 {
		fmt.Fprintf(&b, " Concentration: <strong>%d</strong> in <code>%s</code>",
			ns[0].Summary.Total, template.HTMLEscapeString(ns[0].Label))
		if len(ns) > 1 {
			fmt.Fprintf(&b, ", <strong>%d</strong> in <code>%s</code>",
				ns[1].Summary.Total, template.HTMLEscapeString(ns[1].Label))
		}
		b.WriteString(".")
	}
	return headline, template.HTML(b.String())
}

// buildAttackGraph composes a 3-column flow diagram (entries → capabilities → impacts) from findings.
// Coordinates are deterministic so the template only emits SVG, no layout computation.
// Returns the cosmetic AttackGraph (for the SVG template) and a detail GraphPayload (for the
// inline JSON block that drives the interactive side panel).
func buildAttackGraph(findings []models.Finding, resourceMap, subjectMap map[string][]models.Finding) (AttackGraph, GraphPayload) {
	emptyPayload := GraphPayload{Glossary: Glossary, Techniques: Techniques, Categories: Categories}

	// Select capabilities: critical + high findings, deduplicated by rule_id (keep highest-scoring), top 10 by score.
	byRule := map[string]models.Finding{}
	for _, f := range findings {
		if f.Severity != models.SeverityCritical && f.Severity != models.SeverityHigh {
			continue
		}
		if existing, ok := byRule[f.RuleID]; !ok || f.Score > existing.Score {
			byRule[f.RuleID] = f
		}
	}
	caps := make([]models.Finding, 0, len(byRule))
	for _, f := range byRule {
		caps = append(caps, f)
	}
	sort.Slice(caps, func(i, j int) bool {
		if caps[i].Severity.Rank() != caps[j].Severity.Rank() {
			return caps[i].Severity.Rank() > caps[j].Severity.Rank()
		}
		if caps[i].Score != caps[j].Score {
			return caps[i].Score > caps[j].Score
		}
		return caps[i].RuleID < caps[j].RuleID
	})
	if len(caps) > 10 {
		caps = caps[:10]
	}
	if len(caps) == 0 {
		return AttackGraph{
			Width: 980, Height: 120,
			Lanes: [3]GraphLane{
				{X: 20, Label: "Entry point", LabelX: 170, LabelW: 110},
				{X: 360, Label: "Abused capability", LabelX: 530, LabelW: 175},
				{X: 720, Label: "Impact", LabelX: 840, LabelW: 80},
			},
		}, emptyPayload
	}

	// Select entry points: the resource/subject each capability is about, deduplicated.
	// Entry key = subject.Key() when present, else resource.Key(). Label reflects source.
	// representative remembers a sample finding so we can later resolve a GlossaryKey from
	// its Subject/Resource without re-walking findings.
	type entryInfo struct {
		Key            string
		Title          string
		Subtitle       string
		Meta           string
		SevClass       string
		topScore       float64
		representative models.Finding
		hasRep         bool
		entryKind      string // "Subject" | "Resource" — used by the JS filter chips.
	}
	entries := map[string]*entryInfo{}
	entryForCap := make([]string, len(caps))
	for i, f := range caps {
		key, title, subtitle := entryIdentity(f)
		if key == "" {
			key, title, subtitle = "cluster", "Cluster-wide", "No subject / resource attributed"
		}
		entryForCap[i] = key
		e, ok := entries[key]
		if !ok {
			kind := ""
			switch {
			case f.Subject != nil:
				kind = "Subject"
			case f.Resource != nil:
				kind = "Resource"
			}
			e = &entryInfo{Key: key, Title: title, Subtitle: subtitle, SevClass: severityClass(f.Severity), entryKind: kind}
			entries[key] = e
		}
		if !e.hasRep {
			e.representative = f
			e.hasRep = true
		}
		if f.Score > e.topScore {
			e.topScore = f.Score
			e.SevClass = severityClass(f.Severity)
		}
	}
	// Count findings attributable to each entry for meta.
	entryCounts := map[string]int{}
	for _, cf := range caps {
		key, _, _ := entryIdentity(cf)
		if key != "" {
			entryCounts[key]++
		}
	}
	for k, e := range entries {
		n := entryCounts[k]
		if n <= 1 {
			e.Meta = "1 critical/high on this entity"
		} else {
			e.Meta = fmt.Sprintf("%d critical/high findings on this entity", n)
		}
	}
	// Order entries by highest capability score descending.
	entryKeys := make([]string, 0, len(entries))
	for k := range entries {
		entryKeys = append(entryKeys, k)
	}
	sort.Slice(entryKeys, func(i, j int) bool {
		ei, ej := entries[entryKeys[i]], entries[entryKeys[j]]
		if ei.topScore != ej.topScore {
			return ei.topScore > ej.topScore
		}
		return ei.Title < ej.Title
	})

	// Select impacts: one per category present in capabilities.
	categoriesPresent := map[models.RiskCategory]bool{}
	for _, f := range caps {
		categoriesPresent[f.Category] = true
	}
	impactOrder := []models.RiskCategory{
		models.CategoryPrivilegeEscalation,
		models.CategoryLateralMovement,
		models.CategoryDataExfiltration,
		models.CategoryInfrastructureModification,
		models.CategoryDefenseEvasion,
	}
	impactKeys := make([]models.RiskCategory, 0, len(categoriesPresent))
	for _, c := range impactOrder {
		if categoriesPresent[c] {
			impactKeys = append(impactKeys, c)
		}
	}

	// Lane geometry.
	const (
		laneEntryX, laneEntryW   = 20, 300
		laneCapX, laneCapW       = 360, 340
		laneImpactX, laneImpactW = 720, 240
		topY                     = 50
		capGap                   = 14
		entryGap                 = 18
		impactGap                = 18
		// Inner text-budget per lane (box width minus left padding minus right breathing room).
		// wrapForWidth uses these to break long strings across multiple lines so the full
		// text shows — this tool is educational, so we never truncate.
		// Entry/cap left padding is 26/22px to clear the severity dot at top-left.
		entryTextPx  = laneEntryW - 26 - 12  // 262
		capTextPx    = laneCapW - 22 - 12    // 306
		impactTextPx = laneImpactW - 16 - 12 // 212
	)

	g := AttackGraph{
		Width: 980,
		Lanes: [3]GraphLane{
			{X: laneEntryX, Label: "Entry point", LabelX: laneEntryX + laneEntryW/2, LabelW: 110},
			{X: laneCapX, Label: "Abused capability", LabelX: laneCapX + laneCapW/2, LabelW: 175},
			{X: laneImpactX, Label: "Impact", LabelX: laneImpactX + laneImpactW/2, LabelW: 80},
		},
	}

	// Build entry nodes with wrapped text + dynamic height.
	entryNodePos := map[string]GraphNode{}
	entryNodeList := make([]GraphNode, 0, len(entryKeys))
	for _, k := range entryKeys {
		e := entries[k]
		// Per-class avg-glyph widths (px) calibrated to the CSS in the template.
		titleLines := wrapForWidth(e.Title, entryTextPx, 7.2)  // node-title:  13px sans 600
		subLines := wrapForWidth(e.Subtitle, entryTextPx, 6.6) // node-sub:    11px monospace
		metaLines := wrapForWidth(e.Meta, entryTextPx, 5.7)    // node-meta:   10.5px sans
		lines, height := composeLines(26, []textCluster{
			{class: "node-title", lineHeight: 17, leadIn: 22, lines: titleLines},
			{class: "node-sub", lineHeight: 14, leadIn: 18, lines: subLines},
			{class: "node-meta", lineHeight: 14, leadIn: 17, lines: metaLines},
		}, 12)
		entryNodeList = append(entryNodeList, GraphNode{
			ID:       "entry-" + slugify(k),
			Kind:     "entry",
			X:        laneEntryX,
			Width:    laneEntryW,
			Height:   height,
			SevClass: e.SevClass,
			Lines:    lines,
		})
	}

	// Build capability nodes.
	capNodes := make([]GraphNode, len(caps))
	for i, f := range caps {
		ruleMeta := fmt.Sprintf("%s  ·  score %.1f", f.RuleID, f.Score)
		ruleLines := wrapForWidth(ruleMeta, capTextPx, 6.3) // rule-id:    10px monospace + letter-spacing
		titleLines := wrapForWidth(f.Title, capTextPx, 7.2) // node-title: 13px sans 600
		lines, height := composeLines(22, []textCluster{
			{class: "rule-id", lineHeight: 14, leadIn: 18, lines: ruleLines},
			{class: "node-title", lineHeight: 17, leadIn: 22, lines: titleLines},
		}, 14)
		capNodes[i] = GraphNode{
			ID:       "cap-" + slugify(f.RuleID+"-"+strconv.Itoa(i)),
			Kind:     "capability",
			X:        laneCapX,
			Width:    laneCapW,
			Height:   height,
			SevClass: severityClass(f.Severity),
			Lines:    lines,
		}
	}

	// Build impact nodes.
	impactNodeList := make([]GraphNode, 0, len(impactKeys))
	for _, c := range impactKeys {
		titleLines := wrapForWidth(impactLabel(c), impactTextPx, 8.5)   // impact-title: 13px bold uppercase
		metaLines := wrapForWidth(impactSubtitle(c), impactTextPx, 5.7) // node-meta:    10.5px sans
		lines, height := composeLines(16, []textCluster{
			{class: "impact-title", lineHeight: 18, leadIn: 26, lines: titleLines},
			{class: "node-meta", lineHeight: 14, leadIn: 18, lines: metaLines},
		}, 14)
		impactNodeList = append(impactNodeList, GraphNode{
			ID:       "imp-" + categoryCSSKey(c),
			Kind:     "impact",
			X:        laneImpactX,
			Width:    laneImpactW,
			Height:   height,
			SevClass: "crit", // impacts rendered with the critical accent gradient
			Lines:    lines,
		})
	}

	// Lane vertical totals (heights sum + gaps). Then center each lane independently
	// in the available column space so short lanes don't bunch at the top.
	laneTotal := func(nodes []GraphNode, gap int) int {
		if len(nodes) == 0 {
			return 0
		}
		total := -gap
		for _, n := range nodes {
			total += n.Height + gap
		}
		return total
	}
	entryTotalH := laneTotal(entryNodeList, entryGap)
	capTotalH := laneTotal(capNodes, capGap)
	impactTotalH := laneTotal(impactNodeList, impactGap)

	height := topY + maxInt(capTotalH, maxInt(entryTotalH, impactTotalH)) + 20
	if height < 260 {
		height = 260
	}
	g.Height = height

	stack := func(nodes []GraphNode, totalH, gap int) []GraphNode {
		startY := topY + (height-topY-20-totalH)/2
		if startY < topY {
			startY = topY
		}
		y := startY
		out := make([]GraphNode, len(nodes))
		for i, n := range nodes {
			n.Y = y
			out[i] = n
			y += n.Height + gap
		}
		return out
	}

	entryNodeList = stack(entryNodeList, entryTotalH, entryGap)
	capNodes = stack(capNodes, capTotalH, capGap)
	impactNodeList = stack(impactNodeList, impactTotalH, impactGap)

	for i, k := range entryKeys {
		entryNodePos[k] = entryNodeList[i]
		g.Nodes = append(g.Nodes, entryNodeList[i])
	}
	for i := range capNodes {
		g.Nodes = append(g.Nodes, capNodes[i])
	}
	impactNodePos := map[models.RiskCategory]GraphNode{}
	for i, c := range impactKeys {
		impactNodePos[c] = impactNodeList[i]
		g.Nodes = append(g.Nodes, impactNodeList[i])
	}

	// Edges: entry → capability (per finding), capability → impact (by category). Each edge gets
	// a stable ID derived from the node IDs so the JS layer can highlight by lookup.
	// edgesByNode tracks which edge IDs touch each node so the side panel can dim everything else
	// when the user hovers/clicks one node.
	edgesByNode := map[string][]string{}
	payload := GraphPayload{Glossary: Glossary, Techniques: Techniques, Categories: Categories}
	for i, f := range caps {
		entryNode, okE := entryNodePos[entryForCap[i]]
		capNode := capNodes[i]
		impNode, okI := impactNodePos[f.Category]
		class := severityClass(f.Severity)
		techKey := TechniqueKeyForFinding(f)
		if okE {
			edgeID := "edge-" + entryNode.ID + "-" + capNode.ID
			g.Edges = append(g.Edges, GraphEdge{
				ID:    edgeID,
				Class: class,
				D:     bezier(entryNode.X+entryNode.Width, entryNode.Y+entryNode.Height/2, capNode.X, capNode.Y+capNode.Height/2),
			})
			payload.Edges = append(payload.Edges, GraphEdgeDetail{
				ID:           edgeID,
				From:         entryNode.ID,
				To:           capNode.ID,
				Class:        class,
				TechniqueKey: techKey,
				ActionLabel:  "abuses",
			})
			edgesByNode[entryNode.ID] = append(edgesByNode[entryNode.ID], edgeID)
			edgesByNode[capNode.ID] = append(edgesByNode[capNode.ID], edgeID)
		}
		if okI {
			edgeID := "edge-" + capNode.ID + "-" + impNode.ID
			g.Edges = append(g.Edges, GraphEdge{
				ID:    edgeID,
				Class: class,
				D:     bezier(capNode.X+capNode.Width, capNode.Y+capNode.Height/2, impNode.X, impNode.Y+impNode.Height/2),
			})
			payload.Edges = append(payload.Edges, GraphEdgeDetail{
				ID:          edgeID,
				From:        capNode.ID,
				To:          impNode.ID,
				Class:       class,
				ActionLabel: "leads to",
			})
			edgesByNode[capNode.ID] = append(edgesByNode[capNode.ID], edgeID)
			edgesByNode[impNode.ID] = append(edgesByNode[impNode.ID], edgeID)
		}
	}

	// Build the per-node detail payload now that edge IDs are known. Glossary keys are derived
	// from the representative finding's Subject/Resource (for entries) or the rule's technique
	// (for capabilities). Categories carry their own explainer copy.
	for _, k := range entryKeys {
		e := entries[k]
		node := entryNodePos[k]
		gloss := ""
		if e.hasRep {
			if key := GlossaryKeyForSubject(e.representative.Subject); key != "" {
				gloss = key
			} else if key := GlossaryKeyForResource(e.representative.Resource); key != "" {
				gloss = key
			}
		}
		payload.Nodes = append(payload.Nodes, GraphNodeDetail{
			ID:          node.ID,
			Kind:        "entry",
			Severity:    e.SevClass,
			Title:       e.Title,
			Subtitle:    e.Subtitle,
			GlossaryKey: gloss,
			EntryKind:   e.entryKind,
			EdgeIDs:     edgesByNode[node.ID],
		})
	}
	for i, f := range caps {
		node := capNodes[i]
		hops := make([]HopView, 0, len(f.EscalationPath))
		for _, h := range f.EscalationPath {
			hops = append(hops, HopView{
				Step:         h.Step,
				From:         h.FromSubject.Key(),
				To:           h.ToSubject.Key(),
				Action:       h.Action,
				TechniqueKey: h.Action, // Techniques map is keyed by the same string
				Permission:   h.Permission,
				Gains:        h.Gains,
			})
		}
		payload.Nodes = append(payload.Nodes, GraphNodeDetail{
			ID:           node.ID,
			Kind:         "capability",
			Severity:     severityClass(f.Severity),
			Title:        f.Title,
			Subtitle:     fmt.Sprintf("score %.1f · %s", f.Score, categoryLabel(f.Category)),
			RuleID:       f.RuleID,
			TechniqueKey: TechniqueKeyForFinding(f),
			Description:  f.Description,
			Remediation:  f.Remediation,
			References:   append([]string(nil), f.References...),
			Hops:         hops,
			RiskCategory: string(f.Category),
			EdgeIDs:      edgesByNode[node.ID],
		})
	}
	for _, c := range impactKeys {
		node := impactNodePos[c]
		payload.Nodes = append(payload.Nodes, GraphNodeDetail{
			ID:           node.ID,
			Kind:         "impact",
			Severity:     "crit",
			Title:        impactLabel(c),
			Subtitle:     impactSubtitle(c),
			RiskCategory: string(c),
			EdgeIDs:      edgesByNode[node.ID],
		})
	}

	return g, payload
}

// entryIdentity returns a stable key + display strings for the entity an attacker would target to exercise this finding.
// Preference: Subject (RBAC identity) > Resource (affected object).
func entryIdentity(f models.Finding) (key, title, subtitle string) {
	if f.Subject != nil {
		k := f.Subject.Key()
		return k, shortKindLabel(f.Subject.Kind, f.Subject.Name), k
	}
	if f.Resource != nil {
		k := f.Resource.Key()
		return k, shortKindLabel(f.Resource.Kind, f.Resource.Name), k
	}
	return "", "", ""
}

